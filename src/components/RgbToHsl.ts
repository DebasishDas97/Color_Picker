export const rgbToHsl = (rgb: number[]): string => {
  let [r, g, b] = rgb;

  r /= 255;
  g /= 255;
  b /= 255;
  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  let h: number | undefined;
  let s: number;
  const l = (max + min) / 2;

  if (max === min) {
    h = 0;
    s = 0;
  } else {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r:
        h = (g - b) / d + (g < b ? 6 : 0);
        break;
      case g:
        h = (b - r) / d + 2;
        break;
      case b:
        h = (r - g) / d + 4;
        break;
    }
    h! /= 6;
  }

  const hslValues = [
    Math.round(h! * 360),
    Math.round(s * 100),
    Math.round(l * 100),
  ];
  return `${hslValues.join(", ")}`;
};
